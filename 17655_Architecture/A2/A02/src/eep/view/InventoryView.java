    private int getProductSelection() {
        int curSel = InventoryView.PROD_CATEGORY_NO_SEL;
        
        if (rdbtnTrees.isSelected()) {
            curSel = InventoryView.PROD_CATEGORY_TREES;
        } else if (rdbtnSeeds.isSelected()) {
            curSel = InventoryView.PROD_CATEGORY_SEEDS;
        } else if (rdbtnShrubs.isSelected()) {
            curSel = InventoryView.PROD_CATEGORY_SHRUBS;
        } else if (rdbtnCultureBoxes.isSelected()) {
            curSel = InventoryView.PROD_CATEGORY_CULTURE_BOXES;
        } else if (rdbtnGenomics.isSelected()) {
            curSel = InventoryView.PROD_CATEGORY_GENOMICS;
        } else if (rdbtnProcessing.isSelected()) {
            curSel = InventoryView.PROD_CATEGORY_PROCESSING;
        } else if (rdbtnRefMaterials.isSelected()) {
            curSel = InventoryView.PROD_CATEGORY_REFERENCE_MATERIALS;
        } else {
            // Empty
        }
        
        return curSel;
    }
    
    private void btnAddItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddItemActionPerformed

        // Adds inventory to database

        Boolean connectError = false;   // Error flag
        Connection DBConn = null;       // MySQL connection handle
        String description;             // Tree, seed, or shrub description
        Boolean executeError = false;   // Error flag
        String errString = null;        // String for displaying errors
        int executeUpdateVal;           // Return value from execute indicating effected rows
        Boolean fieldError = false;     // Error flag
        String msgString = null;        // String for displaying non-error messages
        ResultSet res = null;           // SQL query result set pointer
        String tableSelected = null;    // String used to determine which data table to use
        Integer quantity;               // Quantity of trees, seeds, or shrubs
        Float perUnitCost;              // Cost per tree, seed, or shrub unit
        String productID = null;        // Product id of tree, seed, or shrub
        java.sql.Statement s = null;    // SQL statement pointer
        String SQLstatement = null;     // String for building SQL queries
        
        // Check to make sure a radio button is selected
       
        txtMessageArea.setText("");
        
        int currProdSel = getProductSelection();
        
        if (currProdSel == InventoryView.PROD_CATEGORY_NO_SEL)
        {
            fieldError = true;
            txtMessageArea.append("\n" + ERR_MSG_INVALID_PROD_SELECTION);
        } else {
       
            //Make sure there is a product description
            if ( txtProdDescription.getText().length() == 0 )
            {
                fieldError = true;
                txtMessageArea.append("\nMust enter a product description.");
                
            } else {
     
                //Make sure there is a product ID
                if ( txtProdID.getText().length() == 0 )
                {
                    fieldError = true;
                    txtMessageArea.append("\nMust enter a product ID.");
                } else {
            
                    //Make sure there is a price
                    if ( txtProdPrice.getText().length() == 0 )
                    {
                        fieldError = true;
                        txtMessageArea.append("\nMust enter a product price.");
                    } else {
                    
                        //Make sure quantity is specified
                        if ( btnProdQuantity.getText().length() == 0 )
                        {
                            fieldError = true;
                            txtMessageArea.append("\nMust enter a product quantity.");
                        } // quantity
                    } // price
                } // product ID
            } //product description
        } //category selected

        //Now, if there was no error in the data fields, we try to
        //connect to the database.
        
        if ( !fieldError )
        {
            try
            {
                msgString = ">> Establishing Driver...";
                txtMessageArea.setText("\n"+msgString);

                //load JDBC driver class for MySQL
                Class.forName( "com.mysql.jdbc.Driver" );

                msgString = ">> Setting up URL...";
                txtMessageArea.append("\n"+msgString);

                //define the data source
                String SQLServerIP = txtDBIP.getText();
                String sourceURL = "jdbc:mysql://" + SQLServerIP + ":3306/" + 
                        this.getInventoryDatabaseName(currProdSel);

                msgString = ">> Establishing connection with: " + sourceURL + "...";
                txtMessageArea.append("\n"+msgString);

                //create a connection to the db
                DBConn = DriverManager.getConnection(sourceURL,"remote","remote_pass");

            } catch (Exception e) {

                errString =  "\nProblem connecting to database:: " + e;
                txtMessageArea.append(errString);
                connectError = true;

            } // end try-catch
        } // fieldError check

        //If there is not connection error, then we form the SQL statement
        //and then execute it.

        if (!connectError && !fieldError )
        {
            try
            {
                // get the data from the text fields
                description = txtProdDescription.getText();
                productID = txtProdID.getText();
                quantity = Integer.parseInt(btnProdQuantity.getText());
                perUnitCost = Float.parseFloat(txtProdPrice.getText());

                // create an SQL statement variable and create the INSERT
                // query to insert the new inventory into the database

                s = DBConn.createStatement();

                // if trees are selected then insert inventory into trees
                // table
                if (currProdSel == InventoryView.PROD_CATEGORY_TREES)
                {
                    SQLstatement = ( "INSERT INTO trees (product_code, " +
                            "description, quantity, price) VALUES ( '" +
                            productID + "', " + "'" + description + "', " +
                            quantity + ", " + perUnitCost + ");");
                    
                    tableSelected = "TREES";

                }

                // if shrubs are selected then insert inventory into strubs
                // table
                if (currProdSel == InventoryView.PROD_CATEGORY_SHRUBS)
                {
                    SQLstatement = ( "INSERT INTO shrubs (product_code, " +
                            "description, quantity, price) VALUES ( '" +
                            productID + "', " + "'" + description + "', " +
                            quantity + ", " + perUnitCost + ");");
                    
                    tableSelected = "SHRUBS";
                }

                // if seeds are selected then insert inventory into seeds
                // table
                if (currProdSel == InventoryView.PROD_CATEGORY_SEEDS)
                {
                    SQLstatement = ( "INSERT INTO seeds (product_code, " +
                            "description, quantity, price) VALUES ( '" +
                            productID + "', " + "'" + description + "', " +
                            quantity + ", " + perUnitCost + ");");
                    
                    tableSelected = "SEEDS";
                }

                // if culture boxes are selected then insert inventory into cultureboxes 
                // table

                if (currProdSel == InventoryView.PROD_CATEGORY_CULTURE_BOXES)
                {
                    SQLstatement = ( "INSERT INTO cultureboxes (productid, " +
                            "productdescription, productquantity, productprice) VALUES ( '" +
                            productID + "', " + "'" + description + "', " +
                            quantity + ", " + perUnitCost + ");");
        
                    tableSelected = "CULTURE BOXES";
                }

                // if processing equipment is selected then insert inventory into strubs
                // table

                if (currProdSel == InventoryView.PROD_CATEGORY_PROCESSING)
                {
                    SQLstatement = ( "INSERT INTO processing (productid, " +
                            "productdescription, productquantity, productprice) VALUES ( '" +
                            productID + "', " + "'" + description + "', " +
                            quantity + ", " + perUnitCost + ");");
                    
                    tableSelected = "PROCESSING";
                }

                // if genomics are selected then insert inventory into the genomics
                // table

                if (currProdSel == InventoryView.PROD_CATEGORY_GENOMICS)
                {
                    SQLstatement = ( "INSERT INTO genomics (productid, " +
                            "productdescription, productquantity, productprice) VALUES ( '" +
                            productID + "', " + "'" + description + "', " +
                            quantity + ", " + perUnitCost + ");");
                
                    tableSelected = "GENOMICS";
                }

                // if reference materials are selected then insert inventory into referencematerials
                // table

                if (currProdSel == InventoryView.PROD_CATEGORY_REFERENCE_MATERIALS)
                {
                    SQLstatement = ( "INSERT INTO referencematerials (productid, " +
                            "productdescription, productquantity, productprice) VALUES ( '" +
                            productID + "', " + "'" + description + "', " +
                            quantity + ", " + perUnitCost + ");");
                    
                    tableSelected = "REFERENCE MATERIALS";
                }
                
                // execute the update
                executeUpdateVal = s.executeUpdate(SQLstatement);

                // let the user know all went well

                txtMessageArea.append("\nINVENTORY UPDATED... The following was added to the " + tableSelected + " inventory...\n");
                txtMessageArea.append("\nProduct Code:: " + productID);
                txtMessageArea.append("\nDescription::  " + description);
                txtMessageArea.append("\nQuantity::     " + quantity);
                txtMessageArea.append("\nUnit Cost::    " + perUnitCost);

            } catch (Exception e) {

                errString =  "\nProblem adding inventory:: " + e;
                txtMessageArea.append(errString);
                executeError = true;

            } // try

        } //execute SQL check

    }//GEN-LAST:event_btnAddItemActionPerformed
    
